    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 
    int response = JOptionPane.showConfirmDialog(this, "Do you really want to clear all data except the first two columns?", "Confirm", JOptionPane.YES_NO_OPTION);

    if (response == JOptionPane.YES_OPTION) {
        try {
            Connection con = ConnectionProvider.getcon();
            DatabaseMetaData metaData = con.getMetaData();
            ResultSet rs = metaData.getColumns(null, null, "attendance", null);

            String[] firstTwoColumns = {"RegisterNumber", "Name"};
            Statement stmt = con.createStatement();

            System.out.println("Fetching current column names...");

            while (rs.next()) {
                String columnName = rs.getString("COLUMN_NAME");
                // Skip the first two columns
                if (!Arrays.asList(firstTwoColumns).contains(columnName)) {
                    System.out.println("Clearing column: " + columnName);
                    String sql = "UPDATE attendance SET " + columnName + " = NULL";
                    stmt.executeUpdate(sql);
                }
            }

            JOptionPane.showMessageDialog(this, "All columns except the first two have been cleared!");

            // Refresh the table data
            loadAttendanceData();

            stmt.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }     // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

