    private void loadAttendanceData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing data

    // SQL query to fetch attendance data
    String query = "SELECT * FROM attendance ORDER BY RegisterNumber ASC"; // Replace with your table name

    try (Connection con = ConnectionProvider.getcon();
         Statement stmt = con.createStatement();
         ResultSet rs = stmt.executeQuery(query)) {

        // Get metadata to determine column count and names
        int columnCount = rs.getMetaData().getColumnCount();
        String[] columnNames = new String[columnCount];

        for (int i = 0; i < columnCount; i++) {
            columnNames[i] = rs.getMetaData().getColumnName(i + 1);
        }
        model.setColumnIdentifiers(columnNames);

        // Fetch data and populate the table model
        while (rs.next()) {
            Object[] rowData = new Object[columnCount];
            for (int i = 0; i < columnCount; i++) {
                rowData[i] = rs.getObject(i + 1);
            }
            model.addRow(rowData);
        }

    } catch (Exception e) {
        e.printStackTrace(); // Handle exceptions appropriately
    }
}


